package TipyDannih.Base;


public class Pract1 {


    public static void main(String[] args) {
        //Целочисленные типы
        
        // 8 бит
        byte maxByte = 127; 
        byte zeroByte = 0;
        
        // 16 бит
        short currentYear = 2025;
        short zeroShort = 0;
        
        // 32 бита 
        int population = 1000000;
        int zeroInt = 0;
        
        // 64 бита
        long earthDistanceKm = 150000000000L; 
        long zeroLong = 0L;
        
        //Типы с плавающей точкой
        
        // 32 бита 
        float piValue = 3.14159f;
        float zeroFloat = 0.0f;
        
        // 64 бита
        double speedOfLight = 299792458.0;
        double zeroDouble = 0.0;
        
        //Символьный тип
        
        // 16 бит
        char firstLetter = 'J';
        char nullChar = '\u0000'; // Нулевой символ Юникода
        
        //Булевский тип
        
        boolean isActive = true;
        boolean isFalse = false;
        
        
    }
}

